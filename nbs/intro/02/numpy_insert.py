import numpy as np

a = np.arange(4)
print(a)
# [0 1 2 3]

print(np.insert(a, 2, 100))
# [  0   1 100   2   3]

print(np.insert(a, 1, [100, 101, 102]))
# [  0 100 101 102   1   2   3]

print(np.insert(a, [0, 2, 4], [100, 101, 102]))
# [100   0   1 101   2   3 102]

_a = a.copy()
_a[1] = 100
print(_a)
# [  0 100   2   3]

_a = a.copy()
_a[1:3] = [100, 101]
print(_a)
# [  0 100 101   3]

# _a = a.copy()
# _a[1] = [100, 101, 102]
# print(_a)
# ValueError: setting an array element with a sequence.

_a = np.insert(a, 1, [100, 101, 102])
_a = np.delete(_a, 4)
print(_a)
# [  0 100 101 102   2   3]

a = np.arange(12).reshape((3, 4))
print(a)
# [[ 0  1  2  3]
